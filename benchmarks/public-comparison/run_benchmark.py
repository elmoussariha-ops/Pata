#!/usr/bin/env python3
import hashlib
import json
import subprocess
import time
from dataclasses import dataclass
from pathlib import Path
from typing import Any

ROOT = Path(__file__).resolve().parents[2]
REPORT_PATH = ROOT / "benchmarks/public-comparison/REPORT.md"


@dataclass
class Scenario:
    key: str
    title: str
    persona: str
    goal: str


SCENARIOS = [
    Scenario(
        key="code_review",
        title="Code review",
        persona="developer",
        goal="Review a Rust patch for ownership and safety regressions",
    ),
    Scenario(
        key="structured_tutoring",
        title="Structured tutoring",
        persona="teacher",
        goal="Explain Rust ownership to a beginner with one guided exercise",
    ),
    Scenario(
        key="smb_support_workflow",
        title="SMB support workflow",
        persona="smb",
        goal="Plan a weekly SMB support workflow to improve customer retention",
    ),
]


def run_once(scenario: Scenario) -> dict[str, Any]:
    cmd = [
        "cargo",
        "run",
        "-q",
        "-p",
        "cli",
        "--",
        "--persona",
        scenario.persona,
        "--goal",
        scenario.goal,
        "--config",
        "config/app.toml",
    ]

    start = time.perf_counter()
    proc = subprocess.run(
        cmd,
        cwd=ROOT,
        capture_output=True,
        text=True,
        check=False,
    )
    elapsed_ms = (time.perf_counter() - start) * 1000

    result: dict[str, Any] = {
        "ok": proc.returncode == 0,
        "elapsed_ms": round(elapsed_ms, 2),
        "stderr": proc.stderr.strip(),
        "stdout": proc.stdout.strip(),
    }

    if proc.returncode == 0:
        try:
            payload = json.loads(proc.stdout)
            result["payload"] = payload
            result["answer_hash"] = hashlib.sha256(payload.get("answer", "").encode()).hexdigest()
        except json.JSONDecodeError as err:
            result["ok"] = False
            result["stderr"] = f"invalid JSON output: {err}"

    return result


def render_report(results: list[dict[str, Any]]) -> str:
    lines: list[str] = []
    lines.append("# Public Comparison Benchmark Report")
    lines.append("")
    lines.append("_Generated by `benchmarks/public-comparison/run_benchmark.py`._")
    lines.append("")
    lines.append("## Method")
    lines.append("")
    lines.append("- 3 representative scenarios: code review, structured tutoring, SMB support workflow.")
    lines.append("- Each scenario is executed **2 times** to evaluate output stability.")
    lines.append("- Metrics are computed from raw runtime outputs only (no synthetic scoring).")
    lines.append("")
    lines.append("## Comparative table")
    lines.append("")
    lines.append("| Scenario | Persona | Run status | Execution time (ms, avg) | Confidence (avg) | Verification rate | Trace events (avg) | Output stability |")
    lines.append("|---|---|---|---:|---:|---:|---:|---|")

    for row in results:
        lines.append(
            "| {title} | `{persona}` | {status} | {t} | {c} | {v} | {e} | {s} |".format(
                title=row["title"],
                persona=row["persona"],
                status=row["status"],
                t=row["execution_ms_avg"],
                c=row["confidence_avg"],
                v=row["verification_rate"],
                e=row["trace_events_avg"],
                s=row["stability"],
            )
        )

    lines.append("")
    lines.append("## Scenario details")
    lines.append("")

    for row in results:
        lines.append(f"### {row['title']} (`{row['persona']}`)")
        lines.append("")
        lines.append(f"- Goal: `{row['goal']}`")
        lines.append(f"- Status: **{row['status']}**")
        lines.append(f"- Execution time avg: `{row['execution_ms_avg']}`")
        lines.append(f"- Confidence avg: `{row['confidence_avg']}`")
        lines.append(f"- Verification rate: `{row['verification_rate']}`")
        lines.append(f"- Trace events avg: `{row['trace_events_avg']}`")
        lines.append(f"- Output stability: `{row['stability']}`")
        if row["notes"]:
            lines.append(f"- Notes: {row['notes']}")
        lines.append("")

    lines.append("## Share-ready snippets")
    lines.append("")
    lines.append("### README/GitHub snippet")
    lines.append("")
    lines.append(
        "`benchmarks/public-comparison/REPORT.md` provides reproducible public comparison results across developer, teacher and smb scenarios with runtime-derived metrics (time, confidence, verification, trace, stability)."
    )
    lines.append("")
    lines.append("### Hacker News / community snippet")
    lines.append("")
    lines.append(
        "We added a public comparison benchmark (code review, tutoring, SMB workflow) that reports only runtime-derived metrics: execution time, confidence, verification rate, trace size, and output stability."
    )
    lines.append("")
    return "\n".join(lines)


def fmt_float(v: float | None) -> str:
    if v is None:
        return "N/A"
    return f"{v:.2f}"


def main() -> int:
    summary_rows: list[dict[str, Any]] = []

    for sc in SCENARIOS:
        runs = [run_once(sc), run_once(sc)]
        ok_runs = [r for r in runs if r["ok"]]

        if ok_runs:
            execution_avg = sum(r["elapsed_ms"] for r in ok_runs) / len(ok_runs)
            confidence_vals = [r["payload"].get("confidence") for r in ok_runs if r.get("payload")]
            confidence_avg = (
                sum(float(x) for x in confidence_vals if isinstance(x, (int, float))) / len(confidence_vals)
                if confidence_vals
                else None
            )
            verification_hits = 0
            trace_counts = []
            hashes = []

            for run in ok_runs:
                payload = run["payload"]
                structured = payload.get("structured_output") or {}
                if structured.get("verification_status") == "Accept":
                    verification_hits += 1
                trace_counts.append(len((structured.get("execution_trace") or {}).get("events") or []))
                hashes.append(run.get("answer_hash"))

            verification_rate = f"{(verification_hits / len(ok_runs)) * 100:.0f}%"
            trace_avg = sum(trace_counts) / len(trace_counts) if trace_counts else None
            stability = "stable" if len(set(hashes)) == 1 and len(hashes) > 1 else "unstable"
            status = f"OK ({len(ok_runs)}/2)"
            notes = ""
        else:
            execution_avg = sum(r["elapsed_ms"] for r in runs) / len(runs)
            confidence_avg = None
            verification_rate = "N/A"
            trace_avg = None
            stability = "N/A"
            status = "FAILED (0/2)"
            err = runs[0].get("stderr") or "unknown error"
            notes = f"Live runtime failed in this environment: `{err.splitlines()[-1]}`"

        summary_rows.append(
            {
                "title": sc.title,
                "persona": sc.persona,
                "goal": sc.goal,
                "status": status,
                "execution_ms_avg": fmt_float(execution_avg),
                "confidence_avg": fmt_float(confidence_avg),
                "verification_rate": verification_rate,
                "trace_events_avg": fmt_float(trace_avg),
                "stability": stability,
                "notes": notes,
            }
        )

    report = render_report(summary_rows)
    REPORT_PATH.write_text(report)
    print(f"Wrote benchmark report: {REPORT_PATH.relative_to(ROOT)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())

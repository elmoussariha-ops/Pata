#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/../.." && pwd)"
TASKS_FILE="${SCRIPT_DIR}/tasks.json"
RESULTS_FILE="${SCRIPT_DIR}/results.json"
REPORT_FILE="${SCRIPT_DIR}/report.md"

if ! command -v cargo >/dev/null 2>&1; then
  echo "error: cargo is required" >&2
  exit 1
fi

if ! command -v python3 >/dev/null 2>&1; then
  echo "error: python3 is required" >&2
  exit 1
fi

python3 - "${REPO_ROOT}" "${TASKS_FILE}" "${RESULTS_FILE}" "${REPORT_FILE}" <<'PY'
import datetime as dt
import json
import subprocess
import sys
import time
from pathlib import Path

repo_root = Path(sys.argv[1])
tasks_file = Path(sys.argv[2])
results_file = Path(sys.argv[3])
report_file = Path(sys.argv[4])

tasks = json.loads(tasks_file.read_text())
results = []

for task in tasks:
    cmd = [
        "cargo", "run", "-q", "-p", "cli", "--",
        "--persona", task["persona"],
        "--goal", task["goal"],
        "--config", "config/app.toml",
    ]

    started = time.perf_counter()
    proc = subprocess.run(cmd, cwd=repo_root, capture_output=True, text=True, check=False)
    duration_ms = round((time.perf_counter() - started) * 1000, 2)

    payload = {}
    parse_error = None
    if proc.returncode == 0:
        try:
            payload = json.loads(proc.stdout)
        except json.JSONDecodeError as err:
            parse_error = f"invalid JSON output: {err}"

    structured = payload.get("structured_output") or {}
    trace_events = (structured.get("execution_trace") or {}).get("events") or []

    tool_calls_count = sum(
        1
        for event in trace_events
        if event.get("event_type") in {"ToolCalled", "ToolReturned"}
    )

    result = {
        "task_id": task["task_id"],
        "success": bool(proc.returncode == 0 and not parse_error and (payload.get("answer") or "").strip()),
        "steps_count": int(structured.get("reasoning_steps_executed") or 0),
        "tool_calls_count": int(tool_calls_count),
        "verification_passed": structured.get("verification_status") == "Accept",
        "final_confidence": float(payload.get("confidence") or 0.0),
        "duration_ms": duration_ms,
    }

    if proc.returncode != 0:
        result["error"] = proc.stderr.strip() or "agent execution failed"
    elif parse_error:
        result["error"] = parse_error

    results.append(result)

success_rate = round(sum(1 for r in results if r["success"]) / len(results), 3) if results else 0.0
verification_rate = round(sum(1 for r in results if r["verification_passed"]) / len(results), 3) if results else 0.0
avg_confidence = round(sum(r["final_confidence"] for r in results) / len(results), 3) if results else 0.0
avg_duration = round(sum(r["duration_ms"] for r in results) / len(results), 2) if results else 0.0

payload = {
    "benchmark_id": "proof-benchmark-v1",
    "generated_at_utc": dt.datetime.now(dt.timezone.utc).isoformat(),
    "deterministic_mode": True,
    "tasks_count": len(results),
    "summary": {
        "success_rate": success_rate,
        "verification_pass_rate": verification_rate,
        "average_confidence": avg_confidence,
        "average_duration_ms": avg_duration,
    },
    "tasks": results,
}

results_file.write_text(json.dumps(payload, indent=2) + "\n")

lines = [
    "# Proof Benchmark Report",
    "",
    "_Generated by `proof/benchmark/run_benchmark.sh`._",
    "",
    "## Benchmark setup",
    "",
    "- Deterministic runtime mode via `config/app.toml`.",
    f"- Standardized task set: **{len(results)}** tasks.",
    "- One run per task for a fast, reproducible baseline.",
    "",
    "## Summary",
    "",
    f"- Success rate: **{success_rate * 100:.1f}%**",
    f"- Verification pass rate: **{verification_rate * 100:.1f}%**",
    f"- Average confidence: **{avg_confidence:.2f}**",
    f"- Average duration: **{avg_duration:.2f} ms**",
    "",
    "## Task results",
    "",
    "| task_id | success | steps_count | tool_calls_count | verification_passed | final_confidence | duration_ms |",
    "|---|---:|---:|---:|---:|---:|---:|",
]

for r in results:
    lines.append(
        f"| `{r['task_id']}` | {str(r['success']).lower()} | {r['steps_count']} | {r['tool_calls_count']} | {str(r['verification_passed']).lower()} | {r['final_confidence']:.2f} | {r['duration_ms']:.2f} |"
    )

lines += [
    "",
    "## Notes",
    "",
    "- `tool_calls_count` is derived from explicit tool events in the execution trace.",
    "- Current baseline may report `0` tool calls when personas solve tasks without tool invocations.",
    "- Use this report to compare regressions between consecutive runs of the same script.",
]

report_file.write_text("\n".join(lines) + "\n")
print(f"Wrote: {results_file}")
print(f"Wrote: {report_file}")
PY

echo "Benchmark complete."
echo "- results: ${RESULTS_FILE}"
echo "- report:  ${REPORT_FILE}"
